    def draw_dice(numbers: list[int]) -> str:
        picture = ""
        if len(numbers) <= 0:
            return ""
        for line in range(5):
            for number in numbers:
                current_dice = Ascii.dice[number]
                picture += current_dice[line]
            picture += '\n'
        return picture
